package com.company.registration.model;

public class UserInformation {
	private String username ;
	private String userEmail;
	private String durationActivate;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getDurationActivate() {
		return durationActivate;
	}
	public void setDurationActivate(String durationActivate) {
		this.durationActivate = durationActivate;
	}
	

}
